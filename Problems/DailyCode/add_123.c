#include <stdio.h>

// 프로그램 설명:
// 이 프로그램은 주어진 정수 n을 1, 2, 3의 합으로 나타내는 방법의 수를 계산합니다.
// 각 합은 하나 이상의 수를 사용하며, 합의 순서가 다른 경우는 다른 방법으로 간주됩니다.
// 예를 들어, 1+2와 2+1은 서로 다른 방법으로 계산됩니다.

int main() {
    int T; // 테스트 케이스의 개수
    scanf("%d", &T); // 첫 번째 줄에서 T를 입력받음

    // dp[i]는 정수 i를 1, 2, 3의 합으로 나타내는 방법의 수를 저장
    // n의 범위가 1 이상 10 이하이므로 dp[11] 크기로 선언
    int dp[11] = {0};

    // 기본 경우 설정:
    // dp[0] = 1: 0을 만드는 방법은 아무것도 선택하지 않는 1가지 방법
    // 이는 점화식을 단순화하기 위해 사용됨
    dp[0] = 1;

    // 동적 계획법(Dynamic Programming)을 이용하여 dp 배열을 채움
    for(int i = 1; i <= 10; i++) {
        // 1을 더하는 경우: i-1을 만드는 방법의 수
        if(i - 1 >= 0) {
            dp[i] += dp[i - 1];
        }
        // 2를 더하는 경우: i-2를 만드는 방법의 수
        if(i - 2 >= 0) {
            dp[i] += dp[i - 2];
        }
        // 3을 더하는 경우: i-3을 만드는 방법의 수
        if(i - 3 >= 0) {
            dp[i] += dp[i - 3];
        }
    }

    // 각 테스트 케이스에 대해 결과를 출력
    for(int test = 0; test < T; test++) {
        int n;
        scanf("%d", &n); // 각 테스트 케이스에서 n을 입력받음

        // n이 1 이상 10 이하임을 보장하므로, dp[n]은 미리 계산된 값을 사용
        printf("%d\n", dp[n]);
    }

    return 0; // 프로그램 종료
}

